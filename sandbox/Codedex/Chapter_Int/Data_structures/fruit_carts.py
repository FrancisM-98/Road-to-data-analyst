my_fruits = {'apple','mango','grapes','ananas','peach'}
fr_fruits = {'banana','orange','apple','kiwi','pear'}

union_our_fruits = my_fruits.union(fr_fruits)
print(union_our_fruits)

intersection_our_fruits = my_fruits.intersection(fr_fruits)
print(intersection_our_fruits)

difference_our_fruits = my_fruits.difference(fr_fruits)
print(difference_our_fruits)
